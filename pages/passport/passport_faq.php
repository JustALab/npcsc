        <h5>Q1. What is a Passport?</h5>

            Ans: A passport is a travel document, usually issued by a country's government that certifies the identity and nationality of its holder.
            <br>
            <br>
         <h5>Q2. Who can get a passport ?</h5>

              Ans: From a age of 0 ( New Born Baby ) with Indian citizenship can apply and get a passport. 
              <br>
              <br>

          <h5>Q3. Is Tatkal Passport Process same as Normal Passport Process?</h5>

            Ans: Under the Tatkaal scheme, an applicant needs to submit additional documents and additional fees along with his/her application form. Also, police verification will be done after the issuance of passport. 
            <br>
            <br>
            <h5>Q4. What is the definition of minor ( Children ) for issue of passport?</h5>

              Ans: Applicants less than 18 years of age are considered as minor for issuance of passport 
              <br>
              <br>
              <h5>Q5. I have a one-month old baby? Does the baby require a separate passport or his/her name can be endorsed in one of the parents passport?</h5>

              Ans: Endorsement of child's name in parent's passports is not allowed anymore. As per the amended instructions , a minor should have a separate passport.
              <br>
              <br>
              <h5>Q6. In case of minor's passport, whose father is in abroad, is it mandatory to obtain father's consent to apply for the child's passport? If so, what is the procedure?</h5>

               Ans: If parent(s) of the minor holds a valid passport, then attested photocopy of passport needs to be submitted along with the application form. Spouse name should be endorsed on the parent's passport. If parent(s) hold a valid passport, but spouse name is not endorsed, then they must get the spouse name added in their passport. For this, they have to apply for re-issue of passport and get the specified change done in personal particulars. If one parent is in abroad, Annexure D OR Annexure C can be submitted by other parent.
               <br>
               <br>
               <h5>Q7. Do I need to provide original documents or will certified copies or extract be enough?<</h5>

                Ans: Applicants are required to attach self -attested photocopies of all the required documents with the application form. Original documents must be produced at the counter; which will be returned after verification.
                <br>
                <br>
                <h5>Q8. Is it mandatory to submit Birth Certificate for applicants who are born on or after 26.01.1989?</h5>

                    Ans: Birth Certificate issued by the Registrar of Births and Deaths or the Municipal Corporation or any other prescribed authority, whosoever has been empowered under the Registration of Birth and Deaths Act, 1969 to register the birth of a child born in India. 
                    Transfer/School leaving/Matriculation Certificate issued by the school last attended/recognised educational board having the date of birth of the applicant.
                    PAN Card issued by the Income Tax Department having the date of birth of the applicant. 
                    Aadhaar Card/E-Aadhaar having the date of birth of the applicant. 
                    Copy of an extract of the service record of the applicant (only in respect of Government servants) or the Pay Pension Order (in respect of retired Government Servants), duly attested/certified by the officer/in-charge of the Administration of the concerned Ministry/Department of the applicant, having his date of birth. 
                    Driving licence issued by the Transport Department of the concerned State Government, having the date of birth of the applicant. 
                    Election Photo Identity Card issued by the Election Commission of India containing the date of birth of the applicant. 
                    Policy Bond issued by Public Life Insurance Corporations/Companies containing the date of birth of the holder of the insurance policy.
                    <br>
                    <br>
                    <h5>Q9. Do I need to be personally present for Police Verification?</h5>

                    Ans: Yes, it would be advisable that you should be physically present at the time of Police Verification
                    <br>
                    <br>
                    <h5>Q10. I have stayed at three different addresses during last one year. Will the police verification be done at all the places?</h5>

                  Ans: Yes, the police verification will be done at all the three addresses where the applicant has resided in the last one year.
                  <br>
                  <br>
                 <h5>Q11. Is police verification done before issuance of passport under the Tatkaal scheme?</h5>

              Ans: No, police verification is not done before issuance of passport under the Tatkaal scheme. In case of the Tatkaal scheme, police verification is done on No or post police verification basis as per the case.
              <br>
              <br>
             <h5> Q12. The applicant is staying at Bangalore for the last two years and permanent address is in Tamilnadu. Can he/she endorse his/her permanent address instead of present address in the passport?</h5>

              Ans: Only the present address will be endorsed on the passport and not the permanent address.
              <br>
              <br>
              <h5>Q13. I have printed my online form and have found a mistake. How can I correct it?</h5>

              Ans: At the Passport Seva Kendra (PSK) please ask the Citizen Service Executive (CSE) to make the required changes in the application form.
              <br>
              <br>
              <h5>Q14. What documents are required when there is a change in name on account of divorce?</h5>

              Ans: Court certified copy of Divorce decree or self attested copy of Divorce certificate What documents have to be submitted for applicants who are unable to provide the prescribed marriage certificate or joint affidavit due to marital discord or separation and are applying for fresh passport? Married applicants would not be required to provide any marriage certificate.
              <br>
              <br>
              <h5>Q15. What are my responsibilities when applying for a passport, if my old passport has been lost or damaged?</h5>

              Ans: Loss of passport should be immediately reported to the nearest Police Station and to the Passport Office (PO) or Indian Mission, if abroad. Also apply for "Re-issue" of passport at your nearest Passport Seva Kendra (PSK) and submit the required documents along with your passport application form. 
              In case of lost passport, you need to apply for re-issue of passport. Following documents must be submitted along with the application: 
              Proof of Present Address 
              Proof of Date of Birth 
              Affidavit stating how and where the passport got lost/damaged (Annexure 'F') 
              Police report in original 
              Self-attested photocopy of first two and last two pages, including ECR/Non-ECR page of old passport, if available.
              <br>
              <br>
              <h5>Q16. What is the validity of passport in India?</h5>

                Ans: An Indian passport is normally valid for 10 years after which the passport holder has to apply for its renewal. However, for a minor, the validity is restricted to 5 years or till they attain the age of 18. Any minor from the age of 15-18 years can either apply for a passport with the validity of 10 years or for a passport which is valid till they attain the age of 18.
                <br>
                <br>
                <h5>Q17. Incorrect details have been printed on my passport. What should I do?</h5>

            Ans: If you find any mistake/error in the particulars printed in the passport booklet and the application form you submitted, please return the passport for necessary rectification. Any additional fees required to be paid would depend on the merits of the case to be decided by the Assistant Passport Officer (APO)/RPO. If there is a misrepresentation of facts, then penalty could also be imposed. However, applicants are requested to apply online with the view to ensure that the passports are delivered without any typographical errors since applicants have themselves fed the data.
